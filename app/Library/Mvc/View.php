namespace Lib\Mvc;


class View extends \Phalcon\Mvc\View
{
    public function __construct( array $options = array() )
    {
        parent::__construct( $options );
    }

    public function partial( $partialPath, $params = null )
    {
        $viewsDir = $this->getViewsDir();
        $partialsDir = $viewsDir. 'partials/';
        $partial = $partialsDir. $partialPath. '.volt';
        $basePartialsDir = $this->getPartialsDir();

        if(file_exists($partial))
            $this->setPartialsDir($partialsDir);

        parent::partial( $partialPath, $params ); // TODO: Change the autogenerated stub

        if(file_exists($partial))
            $this->setPartialsDir($basePartialsDir);
    }

}